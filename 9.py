import time

numbers = [
    "------------|||||||| |||||||| |||||||| |||||||| |||||||| ||||||||------------",
    "------------||       ||    ||       || ||    || ||    || ||    ||------------",
    "------------|||||||| |||||||| |||||||| |||||||| ||    || ||||||||------------",
    "------------      ||       || ||             || ||    ||       ||------------",
    "------------|||||||| |||||||| |||||||| |||||||| |||||||| ||||||||------------"
]

def print_matrix(matrix):
    for row in matrix:
        for char in row:
            print(char, end='', flush=True)
            time.sleep(0.05)  # 控制字符打印速度
        print()  # 换行

print_matrix(numbers)

